% DISPLAY
%
% This directory contains modules associated with displaying data visually,
% either on the current computer (e.g. SCOPEDISPLAY, STATSDISPLAY,
% SWEEPDISPLAY) or on remote computers, using the F21CONTROL class.
%
% Files
%   f21com       - Basic tool for sending and receiving data from remote f21.
%   hline        - Draws a horizontal line on axes.
%   ScopeDisplay - Display module for plotting data continously.
%   StatsDisplay - Display module for plotting statistics
%   SweepDisplay - Display module for plotting sweeps
%   vline        - Draws a vertical line on axes.
%
% $Id: Contents.m,v 1.1 2006/01/24 03:26:04 meliza Exp $